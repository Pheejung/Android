package com.example.userinterface1;

public class Item {

    String ITEM_NAME;
    String MATERIAL_NAME;
    String STORAGE_METHOD;
    String CHART;
    String ITEN_SEQ;

    public String getITEM_NAME() {
        return ITEM_NAME;
    }

    public void setITEM_NAME(String ITEM_NAME) {
        this.ITEM_NAME = ITEM_NAME;
    }

    public String getMATERIAL_NAME() {
        return MATERIAL_NAME;
    }

    public void setMATERIAL_NAME(String MATERIAL_NAME) {
        this.MATERIAL_NAME = MATERIAL_NAME;
    }

    public String getSTORAGE_METHOD() {
        return STORAGE_METHOD;
    }

    public void setSTORAGE_METHOD(String STORAGE_METHOD) {
        this.STORAGE_METHOD = STORAGE_METHOD;
    }

    public String getCHART() {
        return CHART;
    }

    public void setCHART(String CHART) {
        this.CHART = CHART;
    }

    public String getITEN_SEQ() {
        return ITEN_SEQ;
    }

    public void setITEN_SEQ(String ITEN_SEQ) {
        this.ITEN_SEQ = ITEN_SEQ;
    }



}
